export function genEmoji() {
  const index = Math.floor(Math.random() * emojis.length);
  return emojis[index];
}

export function genEmojis(length: number) {
  let emojis = '';

  for (let i = 0; i < length; i++) {
    emojis += genEmoji();
  }

  return emojis;
}

